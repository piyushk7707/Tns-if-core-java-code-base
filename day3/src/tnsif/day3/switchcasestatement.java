package tnsif.day3;

public class switchcasestatement {

	public static void main(String[] args) {
	int userinput=6;
	switch(userinput)
	{
	case 1:System.out.println("current implementing case is 1");
	break;

	case 2:System.out.println("current implementing case is 2");
	break;

	case 3:System.out.println("current implementing case is 3");
	break;

	case 4:System.out.println("current implementing case is 4");
	break;
	default :
		System.out.println("current implementing case is default");
	}
	}

}
